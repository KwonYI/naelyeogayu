<script>
export default {
  data: function () {
    return {
      agree1: false,
      agree2: false,
      agreeall: false,
    };
  },
  methods: {
    checkAll(checked) {
      this.agreeall = checked;
      this.agree1 = checked;
      this.agree2 = checked;
    },
    check(num) {
      if (num === 1) {
        this.agree1 = !this.agree1;
        if (this.agree1 && this.agree2) {
          this.agreeall = true;
        }
      } else if (num === 2) {
        this.agree2 = !this.agree2;
        if (this.agree1 && this.agree2) {
          this.agreeall = true;
        }
      }
      if (!this.agree1 || !this.agree2) this.agreeall = false;
    },
    nextpage() {
      this.$emit("agreeall", this.agreeall);
    },
  },
};
</script>

